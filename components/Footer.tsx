function Links({
	links,
}: {
	links: { href: string; name: string; target?: string; external?: boolean }[];
}) {
	return (
		<>
			{links.map(({ href, name, target, external }) => {
				return (
					<span key={name + href}>
						{target ? (
							<a href={href} target={target}>
								{name}
								{external && (
									<span className={styles.arrow}>
										<MiniArrow />
									</span>
								)}
							</a>
						) : (
							<Link href={href}>
								<a>
									{name}
									{external && (
										<span className={styles.arrow}>
											<MiniArrow />
										</span>
									)}
								</a>
							</Link>
						)}
					</span>
				);
			})}
		</>
	);
}

